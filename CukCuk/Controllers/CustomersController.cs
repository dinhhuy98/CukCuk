using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Http;
using Microsoft.AspNetCore.Mvc;
using Microsoft.EntityFrameworkCore;
using CukCuk.Model;
using System.IO;
using System.Reflection;

namespace CukCuk.Controllers
{
    [Route("api/v1/[controller]")]
    [ApiController]
    public class CustomersController : ControllerBase
    {
        private readonly CustomerdbContext _context;

        public CustomersController(CustomerdbContext context)
        {
            _context = context;
        }

        // GET: api/Customers1
        [HttpGet]
        public async Task<ActionResult<IEnumerable<Customer>>> GetCustomer([FromQuery] String page)
        {
            if (page!=null)
                return await _context.Customer.ToListAsync();
            else
                return null;
        }

        [HttpGet("test")]
        public String test([FromQuery] String page)
        {
            if(page==null)
            return "fff";
            return page;

        }

        // GET: api/Customers1/5
        [HttpGet("{id}")]
        public async Task<ActionResult<Customer>> GetCustomer(Guid id)
        {
            var customer = await _context.Customer.FindAsync(id);

            if (customer == null)
            {
                return NotFound();
            }

            return customer;
        }

        // PUT: api/Customers1/5
        // To protect from overposting attacks, enable the specific properties you want to bind to, for
        // more details, see https://go.microsoft.com/fwlink/?linkid=2123754.
        [HttpPut("{id}")]
        public async Task<IActionResult> PutCustomer(Guid id, Customer customer)
        {
            if (id != customer.CustomerId)
            {
                return BadRequest();
            }

            _context.Entry(customer).State = EntityState.Modified;

            try
            {
                await _context.SaveChangesAsync();
            }
            catch (DbUpdateConcurrencyException)
            {
                if (!CustomerExists(id))
                {
                    return NotFound();
                }
                else
                {
                    throw;
                }
            }

            return NoContent();
        }

        // POST: api/Customers1
        // To protect from overposting attacks, enable the specific properties you want to bind to, for
        // more details, see https://go.microsoft.com/fwlink/?linkid=2123754.
        [HttpPost]
        public async Task<ActionResult<Customer>> PostCustomer(Customer customer)
        {

            _context.Customer.Add(customer);
            await _context.SaveChangesAsync();
            
            return CreatedAtAction("GetCustomer", new { id = customer.CustomerId }, customer);
        }

        [HttpPost("uploadimg/{imgname}")]
        public async Task<IActionResult> ImageUpload(IFormFile image,String imgname)
        {
            String[] arrTemp = image.FileName.Split('.');
            imgname = imgname +"."+ arrTemp[arrTemp.Length - 1];
            var path = Path.Combine(Directory.GetCurrentDirectory(), "wwwroot","upload", imgname);
            using(System.IO.Stream stream = new FileStream(path, FileMode.Create))
            {
                await image.CopyToAsync(stream);
            }
            return NoContent();

        }

        // DELETE: api/Customers1/5
        [HttpDelete("{id}")]
        public async Task<ActionResult<Customer>> DeleteCustomer(Guid id)
        {
            var customer = await _context.Customer.FindAsync(id);
            if (customer == null)
            {
                return NotFound();
            }

            _context.Customer.Remove(customer);
            await _context.SaveChangesAsync();

            return customer;
        }

        private bool CustomerExists(Guid id)
        {
            return _context.Customer.Any(e => e.CustomerId == id);
        }
    }
}
